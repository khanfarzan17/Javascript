// Switch Statement is being used in Multiple If Else  Statment//

let month=11

switch (month) {
    case 1: 
    console.log("Jan")
         break;
    case 2: 
    console.log("Feb")
         break;
    case 3: 
    console.log("March")
        break;
    case 4: 
    console.log("April")
        break;
    default:
        console.log("Month not Found")
        break;
}